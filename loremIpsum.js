// Create a method that prints out Lorem ipsum sentence (”Lorem Ipsum dolor sit amet, consectetura dipiscing elit. ”) ten times. 
function tenLoremIpsums() {
    for ( let i = 0; i < 10; i++ ) {
        process.stdout.write("\nLorem Ipsum dolor sit amet, consectetura dipiscing elit.");
    }
}
// Call this method.
tenLoremIpsums();